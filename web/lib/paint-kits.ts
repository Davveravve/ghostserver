// Get paint kit for a weapon and skin name
export function getPaintKit(weapon: string, skinName: string): number {
  // Try full "Weapon | Skin" format first
  const fullKey = `${weapon} | ${skinName}`
  if (paintKits[fullKey] !== undefined) {
    return paintKits[fullKey]
  }

  // For knives and gloves, try just the skin name (generic patterns)
  if (paintKits[skinName] !== undefined) {
    return paintKits[skinName]
  }

  return 0
}

// Get weapon def index for a weapon name
export function getWeaponDefIndex(weaponName: string): number {
  return weaponDefIndex[weaponName] || 0
}

